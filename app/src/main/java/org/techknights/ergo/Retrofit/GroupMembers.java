public class GroupMembers {
            String  id;
           String name;
           String email;
           String profile_pic;

    public GroupMembers() {
        this.id = id;
        this.name = name;
        this.email = email;
        this.profile_pic = profile_pic;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getProfile_pic() {
        return profile_pic;
    }
}
